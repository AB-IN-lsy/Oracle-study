CREATE TABLE STUDENT(
  Sno CHAR(9) PRIMARY KEY,
  Sname CHAR(20),
  Sage INT,
  Ssex CHAR(2),
  Sdept CHAR(20)
);

CREATE TABLE COURSE(
  Cno CHAR(9) PRIMARY KEY,
  Cname CHAR(20) NOT NULL,
  Cpno CHAR(4) REFERENCES COURSE(Cno),
  Ccredit INT
);

CREATE TABLE SC(
  Sno CHAR(9),
  Cno CHAR(9),
  Grade INT,
  PRIMARY KEY (Sno, Cno),
  FOREIGN KEY (Sno) REFERENCES STUDENT(Sno),
  FOREIGN KEY (Cno) REFERENCES COURSE(Cno)
);

